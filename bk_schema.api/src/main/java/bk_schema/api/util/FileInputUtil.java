package bk_schema.api.util;

public class FileInputUtil {
	
	private String error;
	private  String[] initialPreview;
	private Object[] initialPreviewThumbTags;
	private String data;
	public String getError() {
		return error;
	}
	public void setError(String error) {
		this.error = error;
	}
	public String[] getInitialPreview() {
		return initialPreview;
	}
	public void setInitialPreview(String[] initialPreview) {
		this.initialPreview = initialPreview;
	}
	public Object[] getInitialPreviewThumbTags() {
		return initialPreviewThumbTags;
	}
	public void setInitialPreviewThumbTags(Object[] initialPreviewThumbTags) {
		this.initialPreviewThumbTags = initialPreviewThumbTags;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	

}
